def _spec_from_v1_dict(idx: int, d: Dict[str, object]) -> MatrixSpec:
    """Convert a v1-style matrix dict into a MatrixSpec with family=v1_structured_random."""
    generator = str(d.get("generator", "structured_random_v1"))
    rows = int(d["rows"])
    cols = int(d["cols"])
    seed = int(d.get("seed", 0))
    name = str(d.get("name") or _default_name(idx, generator, rows, cols, seed)).strip()
    tags = d.get("tags", [])
    if tags is None:
        tags = []
    if not isinstance(tags, list):
        raise ValueError("tags must be a list of strings")

    # Pack original dict into family_params so v1_compat can read it
    family_params = dict(d)

    return MatrixSpec(
        name=name,
        family="v1_structured_random",
        variant="",
        family_number="",
        rows=rows,
        cols=cols,
        seed=seed,
        tags=list(tags),
        family_params=family_params,
        postprocess={},
        value_mode=str(d.get("value_mode", "ones")),
    )


def _spec_from_v2_dict(d: Dict[str, object]) -> MatrixSpec:
    """Convert a v2-style matrix dict into a MatrixSpec."""
    name = str(d["name"]).strip()
    if not name:
        raise ValueError("v2 matrix spec requires a non-empty 'name'")
    family = str(d["family"]).strip()
    variant = str(d.get("variant", "")).strip()
    family_number = str(d.get("family_number", "")).strip()
    rows = int(d["rows"])
    cols = int(d["cols"])
    seed = int(d.get("seed", 0))
    tags = d.get("tags", [])
    if tags is None:
        tags = []
    if not isinstance(tags, list):
        raise ValueError("tags must be a list of strings")
    family_params = dict(d.get("family_params", {}))
    postprocess = dict(d.get("postprocess", {}))
    value_mode = str(d.get("value_mode", "ones")).strip()

    return MatrixSpec(
        name=name,
        family=family,
        variant=variant,
        family_number=family_number,
        rows=rows,
        cols=cols,
        seed=seed,
        tags=list(tags),
        family_params=family_params,
        postprocess=postprocess,
        value_mode=value_mode,
    )


def _read_spec(path: Path) -> Tuple[List[MatrixSpec], List[Dict[str, object]]]:
    """Parse spec file, return (matrix_specs, raw_pairs)."""
    with path.open("r", encoding="utf-8") as handle:
        payload = json.load(handle)

    version = int(payload.get("version", 1))
    if "matrices" not in payload or not isinstance(payload["matrices"], list):
        raise ValueError("spec must include a 'matrices' list")

    specs: List[MatrixSpec] = []
    if version == 1:
        for idx, d in enumerate(payload["matrices"]):
            if not isinstance(d, dict):
                raise ValueError("Each matrix spec entry must be a JSON object")
            specs.append(_spec_from_v1_dict(idx, d))
    elif version == 2:
        for d in payload["matrices"]:
            if not isinstance(d, dict):
                raise ValueError("Each matrix spec entry must be a JSON object")
            specs.append(_spec_from_v2_dict(d))
    else:
        raise ValueError(f"Unsupported spec version {version}")

    return specs, payload.get("pairs", [])


