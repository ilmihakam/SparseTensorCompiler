# Generation
# ---------------------------------------------------------------------------

def generate_one(spec: MatrixSpec) -> GeneratedMatrix:
    """Generate a single matrix from a MatrixSpec."""
    generator_fn = get_generator(spec.family)
    rng = random.Random(spec.seed)
    matrix = generator_fn(spec, rng)
    matrix = apply_postprocess(matrix, spec.postprocess, rng, spec.value_mode)
    return matrix


